  },
  methods: {
    goto(url) {
      window.open(url, "_blank");
    }
  },
  computed: {
    filteredData() {
      if (this.activefilter === 'all') return this.scripts

      let scripts = {}

      for (const key in this.scripts) {
        const script = this.scripts[key];
        let hasFilter = script.categories.find((a) => {
          return a === this.activefilter
        })

        if (hasFilter) scripts[key] = script
      }

      return scripts
    }
  }
